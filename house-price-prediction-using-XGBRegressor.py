# Import Libraries 
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
import sklearn.datasets
from sklearn.model_selection import train_test_split
from xgboost import XGBRegressor
from sklearn import metrics

# load boston house prise dataset
house_price_dataset = sklearn.datasets.load_boston()

print(house_price_dataset)

# loading the dataset to pandas dataframe
house_prise_dataframe = pd.DataFrame(house_prise_dataset.data,columns=house_prise_dataset.feature_names)

house_prise_dataframe.head()

# daa target col to dataframe
house_prise_dataframe['price'] = house_price_dataset.target

house_prise_dataframe.head()

house_prise_dataframe.shape

house_prise_dataframe.isnull().sum()

house_prise_dataframe.describe()

# understanding the correllation betwen the varis future in dataset
# .Postive # .Negative
correlation = house_prise_dataframe.corr()

# constructing a heatmap to nderstand the correlation
plt.figure(figsize=(10,10))
sns.heatmap(correlation, cbar=True, square=True, fmt='.1f', annot=True, annot_kws={'size':8}, cmap='Blues')

X = house_prise_dataframe.drop(['price'], axis=1)
Y = house_prise_dataframe['price']

print(X)
print(Y)

X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size = 0.2, random_state = 2)

print(X.shape, X_train.shape, X_test.shape)

print(Y.shape, Y_train.shape, Y_test.shape)

# loading model
model = XGBRegressor()

model.fit(X_train,Y_train)

# accuracy for prediction on training data
training_data_prediction = model.predict(X_train)

print(training_data_prediction)

# R squared error
score_1 = metrics.r2_score(Y_train, training_data_prediction)

# Mean Absolute Error
score_2 = metrics.mean_absolute_error(Y_train, training_data_prediction)

print("R squared error : ", score_1)
print('Mean Absolute Error : ', score_2)

plt.scatter(Y_train, training_data_prediction)
plt.xlabel("Actual Prices")
plt.ylabel("Predicted Prices")
plt.title("Actual Price vs Preicted Price")
plt.show()

# accuracy for prediction on test data
test_data_prediction = model.predict(X_test)

# R squared error
score_1 = metrics.r2_score(Y_test, test_data_prediction)

# Mean Absolute Error
score_2 = metrics.mean_absolute_error(Y_test, test_data_prediction)

print("R squared error : ", score_1)
print('Mean Absolute Error : ', score_2)